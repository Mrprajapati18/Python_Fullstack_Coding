a,b,c =10, 5,15;
print(a);
print(b);

# Arithmetic operator
print(a+b);
print(a-b);
print(a*b);
print(a/b);
print(a%b);
print(a ** b);

# comparison Operator

print(a==b);
print(a>c);

print(a<c);
print(a>b);

print(a>=c);
print(a<=b);

print(a !=b);


# Logical Operator (And)
print(10>5 and 15>5);
print(10>5 and 15>50);
print(10>55 and 15>5);
print(10>50 and 15>50);


# Logical Operator (Or)
print(10>5 or 15>5);    #true or true

print(10>5 or 15>50);   # true or false

print(10>55 or 15>5);   # false or true

print(10>50 or 15>50);  # false or false

# Logical Operator(Not)

print(not(10>5));   #not true

print(not(20<5));   # not false